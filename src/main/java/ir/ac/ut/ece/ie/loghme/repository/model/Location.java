package ir.ac.ut.ece.ie.loghme.repository.model;

public class Location {
    private int x;
    private int y;


    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
